test("every lib symbol the CLI and hooks import is actually exported (boundary smoke)", async () => {
  const sources = [
    path.join(ROOT, "bin", "cf.mjs"),
    path.join(ROOT, "scripts", "session-start-hook.mjs"),
    path.join(ROOT, "scripts", "user-prompt-hook.mjs"),
  ];
  const importRe = /import\s+\{([^}]+)\}\s+from\s+"(\.\.\/lib\/[^"]+|\.\/hook-io\.mjs)"/g;
  let checked = 0;
  for (const source of sources) {
    const src = await readFile(source, "utf8");
    let match;
    while ((match = importRe.exec(src))) {
      const symbols = match[1].split(",").map((s) => s.trim().split(/\s+as\s+/)[0].trim()).filter(Boolean);
      const target = match[2].startsWith("../lib/")
        ? path.join(ROOT, "lib", path.basename(match[2]))
        : path.join(ROOT, "scripts", "hook-io.mjs");
      const loaded = await import(target);
      for (const symbol of symbols) {
        assert.notEqual(loaded[symbol], undefined, `${match[2]} must export ${symbol} (imported by ${path.basename(source)})`);
        checked += 1;
      }
    }
  }
  assert.ok(checked >= 15, `expected to verify several imports, got ${checked}`);
});
