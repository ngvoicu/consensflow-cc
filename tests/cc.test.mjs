test("parseTranscriptJsonl tolerates garbage lines and collectHandoff degrades to empty", async () => {
  const entries = parseTranscriptJsonl(`{"type":"user","message":{"role":"user","content":"hi"}}\nnot json\n\n{"type":"assistant","message":{"role":"assistant","content":[{"type":"text","text":"yo"}]}}`);
  assert.equal(entries.length, 2);
  await withTempDir(async (dir) => {
    // No session stash at all -> empty handoff, no throw.
    assert.equal(await collectHandoff(dir), "");
    // Stash pointing at a missing transcript -> still empty, no throw.
    await saveSession(dir, { transcriptPath: path.join(dir, "missing.jsonl") });
    assert.equal(await collectHandoff(dir), "");
    // Real transcript -> serialized handoff.
    const transcriptPath = path.join(dir, "t.jsonl");
    await writeFile(transcriptPath, `${JSON.stringify({ type: "user", message: { role: "user", content: "let's design the cache" }, isSidechain: false })}\n`, "utf8");
    await saveSession(dir, { transcriptPath });
    assert.match(await collectHandoff(dir), /User:\nlet's design the cache/);
  });
});

// --- Hook scripts ----------------------------------------------------------

function hookEnv(dir, extra = {}) {
  return { CONSENSFLOW_HOME: path.join(dir, "home"), ...extra };
}

async function runHook(script, payload, env) {
  return await spawnWithInput(process.execPath, [path.join(ROOT, "scripts", script)], {
    input: JSON.stringify(payload),
    env,
    timeoutMs: 15000,
  });
}

async function addParticipant(dir, preset, env = {}) {
  const result = await spawnWithInput(process.execPath, [CF, "participants", "add", preset], {
    cwd: dir,
    env: hookEnv(dir, env),
    timeoutMs: 15000,
  });
  assert.equal(result.exitCode, 0, result.stderr);
}

test("session-start hook stashes the transcript path and emits a roster context", async () => {
  await withTempDir(async (dir) => {
    await addParticipant(dir, "zeus");
    const result = await runHook("session-start-hook.mjs", { cwd: dir, session_id: "s-1", transcript_path: "/tmp/tr.jsonl", source: "startup" }, hookEnv(dir));
    assert.equal(result.exitCode, 0);
    assert.match(result.stdout, /ConsensFlow is available/);
    assert.match(result.stdout, /@zeus \(claude-code claude-opus-4-8\)/);
    assert.match(result.stdout, /never apply a participant's advice/);
    assert.match(result.stdout, /bin\/cf\.mjs/);
    const oldHome = process.env.CONSENSFLOW_HOME;
    process.env.CONSENSFLOW_HOME = path.join(dir, "home");
    try {
      const session = await loadSession(dir);
      assert.equal(session.sessionId, "s-1");
      assert.equal(session.transcriptPath, "/tmp/tr.jsonl");
    } finally {
      if (oldHome === undefined) delete process.env.CONSENSFLOW_HOME;
      else process.env.CONSENSFLOW_HOME = oldHome;
    }
  });
});

